/*
Rock, Y for Paper, and Z for Scissors
The score for a single round is the score for the shape you selected 
(1 for Rock, 2 for Paper, and 3 for Scissors) 
plus the score for the outcome of the round 
(0 if you lost, 3 if the round was a draw, and 6 if you won).

RP = 2+6 = 8
RS = 3+0 = 3
RR = 1+3 = 4
PR = 1+0 = 1
PS = 3+6 = 9
PP = 2+3 = 5
SR = 1+6 = 7
SP = 2+0 = 2
SS = 3+3 = 6
*/

DECLARE @CRLF varchar(10) = char(13) + char(10) ;
DECLARE @inStr varchar(max) = REPLACE(@inp,@CRLF,'|')
SET @inStr = REPLACE(@inStr,' ','')

DROP TABLE IF EXISTS #tmpRPS
CREATE TABLE #tmpRPS (rpsCall varchar(10), p1Call varchar(5), p2Call varchar(5), score int)
INSERT INTO #tmpRPS (rpsCall)
SELECT value FROM STRING_SPLIT(@inStr,'|')

UPDATE #tmpRPS
SET p1Call = LEFT(rpsCall,1)
    , p2Call = RIGHT(rpsCall,1)


/* PART 1 */
UPDATE #tmpRps
SET score = CASE 
                WHEN rpsCall = 'AY' THEN 8
                WHEN rpsCall = 'AZ' THEN 3
                WHEN rpsCall = 'AX' THEN 4
                WHEN rpsCall = 'BX' THEN 1
                WHEN rpsCall = 'BZ' THEN 9
                WHEN rpsCall = 'BY' THEN 5
                WHEN rpsCall = 'CX' THEN 7
                WHEN rpsCall = 'CY' THEN 2
                WHEN rpsCall = 'CZ' THEN 6
            END

SELECT sum(score) FROM #tmpRPS


/* PART 2 */ /* X=lose, Y=draw, Z=win */
UPDATE #tmpRps
SET score = CASE 
                WHEN rpsCall = 'AX' THEN 3
                WHEN rpsCall = 'AY' THEN 4
                WHEN rpsCall = 'AZ' THEN 8
                WHEN rpsCall = 'BX' THEN 1
                WHEN rpsCall = 'BY' THEN 5
                WHEN rpsCall = 'BZ' THEN 9
                WHEN rpsCall = 'CX' THEN 2
                WHEN rpsCall = 'CY' THEN 6
                WHEN rpsCall = 'CZ' THEN 7
            END
SELECT sum(score) FROM #tmpRPS
